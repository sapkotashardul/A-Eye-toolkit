    class Installer implements Runnable {

        int k = 0;
        
        @Override
        public void run() {
            ProcessBuilder pb = null;
            switch (os) {
                case "Windows 10" : 
                    pb = new ProcessBuilder("cmd","/c",command);
                    break;
                case "MacOs" : 
                    pb = new ProcessBuilder("/bin/bash","-c",command);
                    break;
                default : break;
            }
            pb.redirectErrorStream(true);
            try {
                //Process i = Runtime.getRuntime().exec("cmd /c start cmd /k \"pip install -U matplotlib && pip install -U h5py\"");
                Process install = pb.redirectErrorStream(true).start();
                BufferedReader reader = new BufferedReader(new InputStreamReader(
                install.getInputStream()));
                String s;
                //int k = 0;
                Boolean error = false;
                while ((s = reader.readLine()) != null) {
                    //k++;
                    if (s.contains("ERROR")) {
                        //errorLabel.setText(s);
                        error = true;
                        l.setText("Failure");
                        l.setForeground(Color.red);
                        l.setVisible(true);
                        p.setVisible(false);
                    } /*else {
                        p.setValue(k);
                    }*/
                    jTextArea1.append(s + "\n");
                    System.out.println(s);
                    //System.out.println(k);
                    //jTextArea1.append("\n" + s);
                }
                if (l.getName().equals("successLabel2") && !error) {
                    successLabel2.setText("Installation successfull");
                    successLabel2.setVisible(true);
                    p.setVisible(false);
                }
                if (l.getName().equals("successLabel1") && !error) {
                    successLabel1.setText("Installation successfull");
                    successLabel1.setVisible(true);
                    p.setVisible(false);
                }
                if (!successLabel1.isVisible() || successLabel1.getText().equals("Failure")) jButton1.setEnabled(true);
                if (!successLabel2.isVisible() || successLabel2.getText().equals("Failure")) jButton2.setEnabled(true);
            } catch (IOException ex) {
                Logger.getLogger(PythonModulesInstaller.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }

